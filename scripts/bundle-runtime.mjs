#!/usr/bin/env node
/**
 * Bundles agent/src/agentHost.ts into a single ESM string and emits it as a TS module
 * that Convex can ship to a Daytona sandbox via the orchestrator.
 *
 * Why bundle? The daemon needs to run as one self-contained file inside the VM. We
 * declare runtime dependencies in agent/package.json, install them with `npm ci` after
 * upload, and the bundle imports them as externals. This is far smaller and cleaner than
 * shipping the entire agent/src tree + tsc-built JS + a tsconfig.
 *
 * Output: convex/runtime/agentHostBundle.generated.ts
 *   - AGENT_HOST_BUNDLE: the bundled JS source as a String.raw template literal.
 *   - AGENT_PACKAGE_JSON: the runtime package.json (so install matches the bundle exactly).
 *   - RUNTIME_VERSION: a stable string the orchestrator stores on conversation rows so we
 *     can detect when an old sandbox is running an out-of-date daemon.
 */

import { build } from "esbuild";
import { mkdir, readFile, writeFile, stat } from "node:fs/promises";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..");

const ENTRY = path.join(ROOT, "agent", "src", "agentHost.ts");
const PKG = path.join(ROOT, "agent", "package.json");
const OUT_DIR = path.join(ROOT, "convex", "runtime");
const OUT_FILE = path.join(OUT_DIR, "agentHostBundle.generated.ts");

async function main() {
  await assertExists(ENTRY, "agent entrypoint");
  await assertExists(PKG, "agent package.json");

  const result = await build({
    entryPoints: [ENTRY],
    bundle: true,
    platform: "node",
    target: "node20",
    format: "esm",
    write: false,
    minify: false,
    sourcemap: false,
    logLevel: "info",
    // Keep runtime deps as externals — they install in the sandbox via `npm ci`.
    external: [
      "@mariozechner/pi-agent-core",
      "@mariozechner/pi-ai",
      "@sinclair/typebox",
      "convex",
      "convex/server",
      "convex/browser",
      "convex/values",
    ],
    banner: {
      // Emit a tiny header so the file is recognizable when tailed in the VM.
      js: "// pi-agent-host bundle — generated by scripts/bundle-runtime.mjs — do not edit",
    },
  });

  const output = result.outputFiles[0];
  if (!output) {
    throw new Error("esbuild produced no output");
  }
  const source = output.text;

  const packageJsonRaw = await readFile(PKG, "utf8");
  const packageJson = JSON.stringify(JSON.parse(packageJsonRaw), null, 2);

  // Hash the bundle + package.json so the orchestrator can compare runtimeVersion across
  // restarts. We slice to 12 chars for readability — collision risk is irrelevant here.
  const hash = createHash("sha256")
    .update(source)
    .update(packageJson)
    .digest("hex")
    .slice(0, 12);
  const runtimeVersion = `${new Date().toISOString().slice(0, 10)}-${hash}`;

  await mkdir(OUT_DIR, { recursive: true });
  const ts = renderModule(source, packageJson, runtimeVersion);
  await writeFile(OUT_FILE, ts, "utf8");

  console.log(
    `[bundle-runtime] wrote ${path.relative(ROOT, OUT_FILE)} (${source.length} bytes, version=${runtimeVersion})`,
  );
}

function renderModule(source, packageJson, version) {
  // JSON.stringify gives us an exact JS string literal for the bundled source, which keeps
  // regex escapes, backticks, and template literals intact inside the generated module.
  return [
    "// AUTO-GENERATED by scripts/bundle-runtime.mjs — do not edit by hand.",
    "// Re-run `npm run bundle:agent` to regenerate.",
    "",
    "export const RUNTIME_VERSION = " + JSON.stringify(version) + ";",
    "",
    "export const AGENT_PACKAGE_JSON = " + JSON.stringify(packageJson) + ";",
    "",
    "export const AGENT_HOST_BUNDLE = " + JSON.stringify(source) + ";",
    "",
  ].join("\n");
}

async function assertExists(p, label) {
  try {
    await stat(p);
  } catch {
    throw new Error(`${label} not found at ${p}`);
  }
}

main().catch((err) => {
  console.error("[bundle-runtime] failed:", err);
  process.exit(1);
});
